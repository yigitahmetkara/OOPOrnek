package oopçalışma;

public class Car {

	private String brand;
	private String model;
	private int year;
	private double price;
	
	public Car( String brand,String model,int year,double price) {
		this.brand=brand;
		this.model=model;
		this.price=price;
		this.year=year;
		
	}
		public String getBrand() {
		return brand;
		}
		public String getModel() {
		return model;
		}
		public int getYear() {
		return year;
		}
		public double getPrice() {
		return price;
		}
	
	
	
	
	
	
	
	
	
	

	}


